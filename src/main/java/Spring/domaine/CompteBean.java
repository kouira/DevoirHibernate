package Spring.domaine;


public class CompteBean {

	
	private String login;

	private String pwd;


	
	
	
	public CompteBean() {
		super();
	}

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	
}
